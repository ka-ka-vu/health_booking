# ==========================================================
# LẤY DANH SÁCH CÂU HỎI
# ==========================================================

def get_questions(specialty):

    return FOLLOWUP.get(

        specialty,

        [

            "Bạn có thể mô tả rõ hơn triệu chứng được không?",

            "Triệu chứng xuất hiện từ khi nào?",

            "Bạn có sốt không?"

        ]

    )


# ==========================================================
# LẤY CÂU HỎI THEO THỨ TỰ
# ==========================================================

def next_question(specialty, step):

    questions = get_questions(specialty)

    if step >= len(questions):

        return None

    return questions[step]


# ==========================================================
# KIỂM TRA ĐÃ HỎI HẾT CHƯA
# ==========================================================

def finished(specialty, step):

    return step >= len(get_questions(specialty))